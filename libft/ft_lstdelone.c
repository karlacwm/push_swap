#include "libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst || !del)
		return ;
	del(lst->content);
	free(lst);
}

// del is a pointer to a function
// that takes a void *
// and returns void

//lst->next is ignored

// assumed that the caller has already fixed the links
